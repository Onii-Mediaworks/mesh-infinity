#[cfg(test)]
mod tests {
    use super::*;

    /// Helper: create a PacketId from a single byte (repeated).
    fn pid(b: u8) -> PacketId {
        PacketId([b; 32])
    }

    #[test]
    fn test_new_cache_empty() {
        let cache = DeduplicationCache::new();
        assert!(cache.is_empty());
        assert_eq!(cache.len(), 0);
    }

    #[test]
    fn test_record_and_check() {
        let mut cache = DeduplicationCache::new();
        let id = pid(0x01);
        let now = 1000;

        // First time: not a duplicate.
        assert!(!cache.is_duplicate(&id));

        // Record it.
        cache.record(id, now);

        // Now it's a duplicate.
        assert!(cache.is_duplicate(&id));
        assert_eq!(cache.len(), 1);
    }

    #[test]
    fn test_check_and_record_combined() {
        let mut cache = DeduplicationCache::new();
        let id = pid(0x02);
        let now = 1000;

        // First call: not duplicate, records it.
        assert!(!cache.check_and_record(id, now));

        // Second call: duplicate.
        assert!(cache.check_and_record(id, now + 1));
    }

    #[test]
    fn test_gc_removes_expired() {
        let mut cache = DeduplicationCache::new();
        let now = 1000;

        // Insert some entries.
        cache.record(pid(0x01), now);
        cache.record(pid(0x02), now);
        cache.record(pid(0x03), now);

        assert_eq!(cache.len(), 3);

        // GC at a time when entries have expired.
        cache.gc(now + DEDUP_WINDOW_SECS + 1);

        assert_eq!(cache.len(), 0);
    }

    #[test]
    fn test_gc_preserves_fresh() {
        let mut cache = DeduplicationCache::new();
        let now = 1000;

        cache.record(pid(0x01), now);
        cache.record(pid(0x02), now + 100);

        // GC at a time when only the first entry is expired.
        // The second entry (at now + 100) should survive.
        let gc_time = now + DEDUP_WINDOW_SECS + 50;
        cache.gc(gc_time);

        // Entry at now is expired (age = DEDUP_WINDOW_SECS + 50 > window).
        // Entry at now+100 has age = DEDUP_WINDOW_SECS - 50, still fresh.
        assert_eq!(cache.len(), 1);
        assert!(!cache.is_duplicate(&pid(0x01)));
        assert!(cache.is_duplicate(&pid(0x02)));
    }

    #[test]
    fn test_different_ids_independent() {
        let mut cache = DeduplicationCache::new();
        let now = 1000;

        cache.record(pid(0x01), now);

        // Different IDs are independent.
        assert!(cache.is_duplicate(&pid(0x01)));
        assert!(!cache.is_duplicate(&pid(0x02)));
        assert!(!cache.is_duplicate(&pid(0xFF)));
    }

    #[test]
    fn test_delivery_receipt_fields() {
        // Just verify the struct can be constructed — the actual
        // signature verification will be tested in the crypto layer.
        let receipt = DeliveryReceipt {
            packet_id: pid(0xAA),
            recipient: [0xBB; 32],
            timestamp: 1000,
            signature: vec![0x42; 64],
        };

        assert_eq!(receipt.packet_id, pid(0xAA));
        assert_eq!(receipt.timestamp, 1000);
    }
}
