#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_packet_roundtrip() {
        let src = [0x11u8; 32];
        let dst = [0x22u8; 32];
        let payload = b"hello mesh";
        let now = 1_000_000u64;

        let pkt = MeshPacket::new(src, dst, PacketKind::Message, payload, now);
        let json = serde_json::to_string(&pkt).unwrap();
        let recovered: MeshPacket = serde_json::from_str(&json).unwrap();

        assert_eq!(recovered.version, 1);
        assert_eq!(recovered.destination, dst);
        assert_eq!(recovered.source, src);
        assert_eq!(recovered.payload_bytes().unwrap(), payload);
        assert_eq!(recovered.ttl, MeshPacket::MAX_TTL);
    }

    #[test]
    fn test_ttl_decrement() {
        let mut pkt = MeshPacket::new([0u8; 32], [1u8; 32], PacketKind::Keepalive, &[], 0);
        assert_eq!(pkt.ttl, MeshPacket::MAX_TTL);
        assert!(pkt.decrement_ttl());
        assert_eq!(pkt.ttl, MeshPacket::MAX_TTL - 1);
    }

    #[test]
    fn test_ttl_zero_drops() {
        let mut pkt = MeshPacket::new([0u8; 32], [1u8; 32], PacketKind::Keepalive, &[], 0);
        pkt.ttl = 0;
        assert!(!pkt.decrement_ttl());
    }
}
