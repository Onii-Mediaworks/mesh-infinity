// Key Change Validation
// ---------------------------------------------------------------------------

/// Validate an incoming key change request from a contact.
///
/// Checks three levels of proof in descending confidence:
/// 1. Old key signature (if present) — cryptographic proof of continuity
/// 2. Passphrase verification (if provided) — out-of-band proof
/// 3. No proof available — require manual acceptance
///
/// All levels start the 72-hour waiting period. The difference is the
/// confidence level returned, which the UI uses to decide how urgently
/// to prompt the user.
///
/// # Arguments
///
/// * `request` — the incoming key change request to validate.
/// * `known_old_pub` — the Ed25519 public key we have on file for this peer.
///
/// # Errors
///
/// Returns `MeshError::InvalidSignature` if the old key signature is present
/// but fails verification (possible MITM or tampering).
/// Returns `MeshError::InvalidKey` if the claimed old key doesn't match
/// our records.
pub fn validate_key_change(
    request: &KeyChangeRequest,
    known_old_pub: &[u8; 32],
) -> Result<KeyChangeValidation, MeshError> {
    // Step 1: Verify the claimed old public key matches what we have on file.
    // A mismatch means either the request is forged or our records are stale.
    // We allow all-zero old_ed_pub as a signal that the old key is unavailable.
    let old_key_available = request.old_ed_pub != [0u8; 32];

    // If the old key is provided in the request, it MUST match our records.
    // An attacker cannot claim a different old key than what we know.
    if old_key_available && request.old_ed_pub != *known_old_pub {
        return Err(MeshError::InvalidKey(
            "old public key in request does not match stored key".to_string(),
        ));
    }

    // Step 2: Check cryptographic proof — old key signature over new keys.
    // This is the strongest proof because only the holder of the old private
    // key could produce a valid signature.
    if let Some(ref sig_bytes) = request.old_key_signature {
        // Reconstruct the signed message: new_ed_pub || new_x25519_pub || timestamp.
        // Must match exactly what create_key_change_request() signed.
        let mut sign_message = Vec::with_capacity(32 + 32 + 8);
        sign_message.extend_from_slice(&request.new_ed_pub);
        sign_message.extend_from_slice(&request.new_x25519_pub);
        sign_message.extend_from_slice(&request.requested_at.to_le_bytes());

        // Verify the signature against the known old public key.
        // Use the domain separator to prevent cross-protocol replay.
        let valid = signing::verify(
            known_old_pub,
            DOMAIN_KEY_CHANGE,
            &sign_message,
            sig_bytes,
        );

        // A present-but-invalid signature is a security event.
        // An attacker may have forged the request or the key is compromised.
        if !valid {
            return Err(MeshError::InvalidSignature);
        }

        // Signature verified — this is high-confidence proof of continuity.
        return Ok(KeyChangeValidation::CryptographicProof);
    }

    // Step 3: Check passphrase proof — Argon2id hash of shared secret.
    // This is medium confidence because it proves knowledge of the passphrase
    // agreed during initial pairing, but does not prove key continuity.
    if request.passphrase_hash.is_some() {
        // Passphrase proof is present. The contact must verify it against
        // their stored verification passphrase using verify_passphrase_hash().
        // We return PassphraseProof to indicate the proof type is available;
        // the actual passphrase comparison happens at a higher layer where
        // the contact's stored passphrase is accessible.
        return Ok(KeyChangeValidation::PassphraseProof);
    }

    // Step 4: No proof available — lowest confidence.
    // The contact must manually verify through an out-of-band channel
    // (e.g., phone call, in-person meeting) before accepting.
    Ok(KeyChangeValidation::NoProof)
}

// ---------------------------------------------------------------------------
// Key Change Actions
// ---------------------------------------------------------------------------

/// Accept a pending key change (contact approves).
///
/// Transitions the request from Pending to Accepted. After acceptance,
/// the caller should update the contact record with the new keys.
/// Only valid when the request is in Pending status.
pub fn accept_key_change(request: &mut KeyChangeRequest) {
    // Only transition from Pending — other states are terminal.
    // Accepting an already-accepted or rejected request is a no-op.
    if matches!(request.status, KeyChangeStatus::Pending { .. }) {
        request.status = KeyChangeStatus::Accepted;
    }
}

/// Reject a pending key change (contact rejects — possible MITM).
///
/// Transitions the request from Pending to Rejected. The peer's keys
/// remain unchanged in the contact record. The contact should be
/// alerted that a suspicious key change was attempted.
pub fn reject_key_change(request: &mut KeyChangeRequest) {
    // Only transition from Pending — other states are terminal.
    // Rejecting an already-accepted change does not revert it.
    if matches!(request.status, KeyChangeStatus::Pending { .. }) {
        request.status = KeyChangeStatus::Rejected;
    }
}

/// Check if a pending key change's 72h wait has expired.
///
/// If the grace period has elapsed and the request is still Pending,
/// it is auto-accepted. This prevents key changes from being stuck
/// in limbo if the contact doesn't respond.
///
/// # Arguments
///
/// * `request` — the key change request to check.
/// * `now_unix` — current Unix timestamp for comparison.
pub fn check_key_change_expiry(request: &mut KeyChangeRequest, now_unix: u64) {
    // Only auto-accept if still Pending and the timer has expired.
    // Already-accepted or rejected requests are not modified.
    if let KeyChangeStatus::Pending { expires_at } = request.status {
        // Compare current time against the expiry deadline.
        // If now >= expires_at, the 72-hour window has closed.
        if now_unix >= expires_at {
            request.status = KeyChangeStatus::AutoAccepted;
        }
    }
}

// ---------------------------------------------------------------------------
// Passphrase Hashing
// ---------------------------------------------------------------------------

/// Derive the Argon2id hash for key change passphrase verification.
///
/// Uses the same Argon2id parameters as PIN derivation (§3.7.4) for
/// consistency: 64 MB memory, 3 iterations, 4 lanes.
///
/// # Arguments
///
/// * `passphrase` — the shared passphrase string to hash.
/// * `salt` — a 16-byte salt (randomly generated per request).
///
/// # Errors
///
/// Returns `MeshError::Internal` if the Argon2id computation fails
/// (should never happen with valid parameters, but we avoid unwrap).
pub fn hash_key_change_passphrase(
    passphrase: &str,
    salt: &[u8; 16],
) -> Result<Vec<u8>, MeshError> {
    // Build the Argon2id instance with spec-mandated parameters.
    // Algorithm::Argon2id is the recommended variant — resistant to both
    // GPU attacks (from Argon2d) and side-channel attacks (from Argon2i).
    let params = Params::new(ARGON2_M_COST, ARGON2_T_COST, ARGON2_P_COST, Some(ARGON2_OUTPUT_LEN))
        .map_err(|e| MeshError::Internal(format!("argon2 params error: {e}")))?;

    // Construct the Argon2id hasher with the configured parameters.
    let argon2 = Argon2::new(Algorithm::Argon2id, Version::V0x13, params);

    // Allocate the output buffer for the hash.
    // 32 bytes is sufficient for comparison-based verification.
    let mut hash_output = vec![0u8; ARGON2_OUTPUT_LEN];

    // Perform the Argon2id computation over the passphrase and salt.
    // This is deliberately slow (memory-hard) to resist brute-force.
    argon2
        .hash_password_into(passphrase.as_bytes(), salt, &mut hash_output)
        .map_err(|e| MeshError::Internal(format!("argon2 hash error: {e}")))?;

    // Return the raw hash bytes for storage/comparison.
    Ok(hash_output)
}

/// Verify a passphrase against a stored Argon2id hash.
///
/// Re-derives the hash from the candidate passphrase using the same
/// salt and compares it to the stored hash in constant time.
///
/// # Arguments
///
/// * `candidate` — the passphrase to verify.
/// * `salt` — the 16-byte salt used when the hash was created.
/// * `stored_hash` — the previously computed Argon2id hash to compare against.
///
/// # Errors
///
/// Returns `MeshError::Internal` if the Argon2id computation fails.
pub fn verify_passphrase_hash(
    candidate: &str,
    salt: &[u8; 16],
    stored_hash: &[u8],
) -> Result<bool, MeshError> {
    // Re-derive the hash from the candidate passphrase with the same salt.
    // The Argon2id parameters must match what was used during creation.
    let candidate_hash = hash_key_change_passphrase(candidate, salt)?;

    // Use constant-time comparison to prevent timing side-channels.
    // An attacker observing response times should not learn whether
    // individual bytes of the hash matched.
    Ok(constant_time_eq(&candidate_hash, stored_hash))
}

/// Constant-time byte comparison to prevent timing side-channels.
///
/// Compares two byte slices in a way that takes the same amount of
/// time regardless of how many bytes match. Critical for comparing
/// hashes where timing leaks could reveal partial matches.
fn constant_time_eq(a: &[u8], b: &[u8]) -> bool {
    // Different lengths are immediately unequal, but we still compare
    // to avoid leaking information about the length difference.
    if a.len() != b.len() {
        return false;
    }

    // XOR all bytes and accumulate — a difference in any byte sets
    // bits in the accumulator. The final result is 0 only if all match.
    let mut diff = 0u8;
    for (x, y) in a.iter().zip(b.iter()) {
        diff |= x ^ y;
    }

    // Zero diff means all bytes matched; non-zero means at least one differed.
    diff == 0
}

