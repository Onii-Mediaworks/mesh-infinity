/// Create a key change request (called by the peer changing their keys).
///
/// Signs the new keys with the old key if `old_secret` is available.
/// If the old key is lost/compromised, pass `None` for `old_secret`
/// and optionally provide a passphrase for out-of-band verification.
///
/// # Arguments
///
/// * `old_secret` — the 32-byte Ed25519 secret key (if still available).
/// * `new_ed_pub` — the new Ed25519 public key to transition to.
/// * `new_x25519_pub` — the new X25519 public key for key agreement.
/// * `passphrase` — optional shared passphrase for out-of-band proof.
///
/// # Errors
///
/// Returns `MeshError::Internal` if Argon2id hashing fails (should not
/// happen with valid parameters, but we never unwrap).
pub fn create_key_change_request(
    old_secret: Option<&[u8; 32]>,
    new_ed_pub: &[u8; 32],
    new_x25519_pub: &[u8; 32],
    passphrase: Option<&str>,
) -> Result<KeyChangeRequest, MeshError> {
    // Get the current Unix timestamp for the request.
    // Uses SystemTime which is monotonic enough for our purposes.
    let now = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map_err(|e| MeshError::Internal(format!("system clock error: {e}")))?
        .as_secs();

    // Build the message to sign: new_ed_pub || new_x25519_pub || timestamp.
    // This binds the signature to both new keys AND the timestamp,
    // preventing an attacker from replaying an old key change signature.
    let mut sign_message = Vec::with_capacity(32 + 32 + 8);
    sign_message.extend_from_slice(new_ed_pub);
    sign_message.extend_from_slice(new_x25519_pub);
    sign_message.extend_from_slice(&now.to_le_bytes());

    // If we have the old secret key, sign the new keys to prove continuity.
    // This is the strongest form of proof — the holder of the old key
    // explicitly authorized the transition to the new keys.
    let old_key_signature = old_secret.map(|secret| {
        signing::sign(secret, DOMAIN_KEY_CHANGE, &sign_message)
    });

    // Derive the old Ed25519 public key from the secret, if available.
    // If the old key is lost, we use a zero placeholder — the contact
    // will compare against their stored key to detect mismatches.
    let old_ed_pub = match old_secret {
        Some(secret) => {
            // Derive the public key from the secret key for the request.
            let signing_key = ed25519_dalek::SigningKey::from_bytes(secret);
            signing_key.verifying_key().to_bytes()
        }
        // Old key is unavailable — zero placeholder signals this to contacts.
        None => [0u8; 32],
    };

    // If a passphrase is provided, hash it with Argon2id for out-of-band
    // verification. Generate a fresh random salt for each request.
    let (passphrase_hash, passphrase_salt) = match passphrase {
        Some(phrase) => {
            // Generate a cryptographically random 16-byte salt.
            let mut salt = [0u8; 16];
            rand_core::OsRng.fill_bytes(&mut salt);

            // Hash the passphrase using Argon2id with the generated salt.
            let hash = hash_key_change_passphrase(phrase, &salt)?;
            (Some(hash), Some(salt))
        }
        // No passphrase — both fields are None.
        None => (None, None),
    };

    // The grace period starts now and expires after 72 hours.
    // During this window, contacts can accept or reject the change.
    let expires_at = now + GRACE_PERIOD_SECS;

    // Assemble the complete key change request.
    Ok(KeyChangeRequest {
        peer_id: PeerId::from_ed25519_pub(&old_ed_pub),
        old_ed_pub,
        new_ed_pub: *new_ed_pub,
        new_x25519_pub: *new_x25519_pub,
        old_key_signature,
        passphrase_hash,
        passphrase_salt,
        requested_at: now,
        status: KeyChangeStatus::Pending { expires_at },
    })
}

