//! Key Change Validation (§4.6)
//!
//! # What is key change validation?
//!
//! When a peer's identity keys change (device loss, replacement, compromise),
//! the system must prevent silent key substitution by an attacker. This module
//! implements the three-layer verification model:
//!
//! 1. **Cryptographic proof**: the OLD key signs the NEW keys, proving the
//!    holder of the old key authorized the transition. Highest confidence.
//! 2. **Passphrase proof**: an Argon2id-hashed passphrase shared out-of-band
//!    during initial pairing. Medium confidence.
//! 3. **No proof**: neither signature nor passphrase is available. The contact
//!    must manually accept or reject the change. Lowest confidence.
//!
//! # 72-hour waiting period
//!
//! All key changes — even those with cryptographic proof — enforce a 72-hour
//! grace period before the new key is trusted. This gives contacts time to
//! detect and reject a compromised key change before it takes effect.
//!
//! # Workflow
//!
//! 1. The peer changing keys calls `create_key_change_request()`.
//! 2. The request is sent to all contacts.
//! 3. Each contact calls `validate_key_change()` to assess proof level.
//! 4. The 72-hour timer starts. Contacts can `accept_key_change()` or
//!    `reject_key_change()` during this window.
//! 5. After 72h, `check_key_change_expiry()` auto-accepts if no action taken.

use argon2::{Algorithm, Argon2, Params, Version};
use serde::{Deserialize, Serialize};

use crate::crypto::signing::{self, DOMAIN_KEY_CHANGE};
use crate::error::MeshError;
use crate::identity::peer_id::PeerId;

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/// The 72-hour waiting period in seconds (§4.6).
/// All key changes must wait this long before being trusted,
/// giving contacts time to detect and reject compromised changes.
const GRACE_PERIOD_SECS: u64 = 72 * 60 * 60;

/// Argon2id memory cost for key change passphrase hashing.
/// 64 MB — same as PIN derivation (§3.7.4) for consistency.
const ARGON2_M_COST: u32 = 65536;

/// Argon2id time cost (iteration count) for passphrase hashing.
/// 3 iterations — spec minimum for interactive use cases.
const ARGON2_T_COST: u32 = 3;

/// Argon2id parallelism for passphrase hashing.
/// 4 lanes — balances speed vs. memory-hardness on typical hardware.
const ARGON2_P_COST: u32 = 4;

/// Output length for the Argon2id passphrase hash.
/// 32 bytes — sufficient for comparing verification hashes.
const ARGON2_OUTPUT_LEN: usize = 32;

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/// A key change request from a peer (§4.6).
///
/// Created when a peer's identity keys change (device loss, replacement,
/// or deliberate rotation). Contains all the information needed for
/// contacts to verify the legitimacy of the change.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct KeyChangeRequest {
    /// The peer whose keys are changing.
    /// Used to look up the existing contact record for verification.
    pub peer_id: PeerId,

    /// The OLD Ed25519 public key (for verification against stored key).
    /// Must match what the contact has on record, or the request is suspicious.
    pub old_ed_pub: [u8; 32],

    /// The NEW Ed25519 public key that the peer wants to transition to.
    /// Will replace old_ed_pub in the contact record after acceptance.
    pub new_ed_pub: [u8; 32],

    /// The NEW X25519 public key for Diffie-Hellman key agreement.
    /// Will replace the old X25519 key in the contact record after acceptance.
    pub new_x25519_pub: [u8; 32],

    /// Proof: signature by the OLD key over (new_ed_pub || new_x25519_pub || timestamp).
    /// If the old key is compromised, this will be absent and the change requires
    /// manual verification by contacts. Provides highest confidence of continuity.
    pub old_key_signature: Option<Vec<u8>>,

    /// Argon2id hash of a passphrase shared out-of-band (for verification
    /// when the old key is unavailable). Medium confidence — proves knowledge
    /// of the shared secret agreed upon during initial pairing.
    pub passphrase_hash: Option<Vec<u8>>,

    /// The salt used for Argon2id passphrase hashing.
    /// Required when passphrase_hash is present; ignored otherwise.
    /// 16 bytes, randomly generated by the key change initiator.
    pub passphrase_salt: Option<[u8; 16]>,

    /// When this request was created (Unix timestamp).
    /// Used as input to the signature message to prevent replay.
    pub requested_at: u64,

    /// Current status of this key change request.
    /// Transitions: Pending -> Accepted | Rejected | AutoAccepted.
    pub status: KeyChangeStatus,
}

/// The lifecycle status of a key change request.
///
/// A request starts as Pending with a 72-hour expiry timer. It can be
/// explicitly accepted or rejected by the contact, or auto-accepted
/// when the grace period expires without action.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub enum KeyChangeStatus {
    /// Waiting for the 72-hour grace period to expire.
    /// `expires_at` is the Unix timestamp when auto-acceptance occurs.
    Pending {
        /// Unix timestamp when the grace period ends.
        expires_at: u64,
    },

    /// The contact has explicitly accepted the key change.
    /// The new keys should now replace the old ones in the contact record.
    Accepted,

    /// The contact has explicitly rejected the key change.
    /// The peer's keys remain unchanged; possible MITM detected.
    Rejected,

    /// The grace period expired and the change was auto-accepted.
    /// Treated the same as Accepted, but distinguishable for audit.
    AutoAccepted,
}

/// The result of validating a key change request.
///
/// Indicates the confidence level of the key change's legitimacy.
/// All levels still enforce the 72-hour waiting period — the difference
/// is whether the contact is prompted for manual action.
#[derive(Clone, Debug, PartialEq, Eq)]
pub enum KeyChangeValidation {
    /// Old key signed the new key — high confidence, still enforce 72h wait.
    /// The holder of the old key explicitly authorized this transition.
    CryptographicProof,

    /// Passphrase matches — medium confidence, enforce 72h wait.
    /// The requester proved knowledge of the shared out-of-band secret.
    PassphraseProof,

    /// No proof available — low confidence, require manual acceptance.
    /// The contact must verify through an out-of-band channel.
    NoProof,
}

