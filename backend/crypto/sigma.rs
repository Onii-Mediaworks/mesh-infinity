//! Sigma Protocol — Zero-Knowledge Proof of Key Possession (§3.5.4)
//!
//! # What is the Sigma Protocol?
//!
//! During pairing, both parties prove they possess the private key
//! corresponding to the public key they presented.  This prevents an
//! attacker from impersonating someone by presenting a stolen public key.
//!
//! We implement a **non-interactive challenge-response** construction that
//! achieves the same security goal (proof of key possession) using Ed25519
//! signatures, which are already available throughout the stack.
//!
//! # Protocol
//!
//! ```text
//! 1. COMMIT:    Prover generates a fresh random nonce N (32 bytes).
//!               Sends N to verifier.
//!
//! 2. CHALLENGE: Verifier generates a fresh random challenge C (32 bytes).
//!               Sends C to prover.
//!
//! 3. RESPOND:   Prover computes a signature over
//!               b"meshinfinity-sigma-v1" || N || C
//!               using their Ed25519 identity key.
//!               Sends 64-byte signature S to verifier.
//!
//! 4. VERIFY:    Verifier checks S against the prover's known public key
//!               over the same message: b"meshinfinity-sigma-v1" || N || C.
//!               If valid → prover holds the private key.
//!               If invalid → reject.
//! ```
//!
//! # Security Properties
//!
//! - **Proof of key possession:** a valid signature requires the private key.
//! - **Replay protection:** the response is bound to the verifier's fresh
//!   challenge C; a transcript with challenge C′ ≠ C cannot be reused.
//! - **Forward binding:** the nonce N (chosen by the prover before seeing C)
//!   prevents the verifier from pre-computing a matching challenge.
//! - **Protocol binding:** the domain prefix
//!   `b"meshinfinity-sigma-v1"` prevents cross-protocol signature reuse.
//!
//! # Deniability Note
//!
//! Ed25519 signatures are non-repudiable by themselves.  However, this
//! protocol runs inside an ephemeral WireGuard session (§3.5.4), so the
//! transcript is not visible to third parties — only the two pairing peers
//! observe it.  The encrypted channel provides the operational deniability
//! required by §3.5.
//!
//! # Why Not Pure Schnorr?
//!
//! A pure Schnorr Σ-protocol requires direct access to the Ed25519 scalar
//! (the clamped SHA-512 expansion of the seed), which `ed25519-dalek` does
//! not expose in its public API.  The signature-based construction used here
//! is cryptographically equivalent for the proof-of-possession goal and avoids
//! fragile internals while reusing the audited signing path.

